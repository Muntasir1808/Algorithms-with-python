# range method in python
# using range we can limit from where to where iterate over
# range(10) this will display numbers from 0 to 9
# range(5,10) this will set the first number as 5 and iterate over till it reaches 9
# we can also tell in what step it should iterate over by range(5,10,2)
# this will iterate by two steps that is 5,7,9

for it in range(10):
    print(it)
print("----This part is terminated---")
for ite in range(5, 10):
    print(ite)
print("----This part is also ended----")
for itm in range(5,10,2):
    print(itm)